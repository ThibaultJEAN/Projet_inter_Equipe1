HAL_StatusTypeDef Write_Register(uint8_t hi2c,static const uint8_t REG,static const uint8_t DATA)
{
	uint8_t data[2];
	data[0]=REG;
	data[1]=DATA;

	return HAL_I2C_Master_Transmit(&hi2c,GAUGE_ADDR,data,2,HAL_MAX_DELAY);

}
